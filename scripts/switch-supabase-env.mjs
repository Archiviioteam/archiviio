import { execSync } from "node:child_process";
import { copyFileSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..");
const envPath = resolve(projectRoot, ".env.local");
const cloudBackupPath = resolve(projectRoot, ".env.local.cloud");

const mode = process.argv[2];

if (mode !== "local" && mode !== "cloud") {
  console.error("Usage: node scripts/switch-supabase-env.mjs <local|cloud>");
  process.exit(1);
}

function parseSupabaseStatusEnv(output) {
  const values = {};

  for (const line of output.split("\n")) {
    const match = line.match(/^([A-Z0-9_]+)="(.*)"$/);
    if (match) values[match[1]] = match[2];
  }

  return values;
}

function writeLocalEnv() {
  let statusOutput;

  try {
    statusOutput = execSync("supabase status -o env", {
      cwd: projectRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
    });
  } catch {
    console.error("Local Supabase is not running. Start it first:");
    console.error("  npm run db:local");
    process.exit(1);
  }

  const status = parseSupabaseStatusEnv(statusOutput);
  const apiUrl = status.API_URL ?? "http://127.0.0.1:54321";
  const anonKey =
    status.ANON_KEY ?? status.PUBLISHABLE_KEY ?? status.SERVICE_ROLE_KEY;
  const serviceRoleKey = status.SERVICE_ROLE_KEY ?? "";

  if (!anonKey) {
    console.error("Could not read local Supabase keys. Run: supabase status -o env");
    process.exit(1);
  }

  if (existsSync(envPath) && !existsSync(cloudBackupPath)) {
    copyFileSync(envPath, cloudBackupPath);
    console.log(`Backed up cloud credentials to .env.local.cloud`);
  }

  const content = `# Local Supabase (Docker) — generated by npm run env:local
# Studio: http://127.0.0.1:54323 | Mailpit: http://127.0.0.1:54324

NEXT_PUBLIC_SUPABASE_URL=${apiUrl}
NEXT_PUBLIC_SUPABASE_ANON_KEY=${anonKey}
SUPABASE_SERVICE_ROLE_KEY=${serviceRoleKey}

# For npm run db:migrate against local Postgres
DATABASE_URL=${status.DB_URL ?? "postgresql://postgres:postgres@127.0.0.1:54322/postgres"}
`;

  writeFileSync(envPath, content, "utf8");
  console.log("Switched .env.local to local Supabase.");
  console.log(`API: ${apiUrl}`);
  console.log("Restore cloud credentials anytime with: npm run env:cloud");
}

function writeCloudEnv() {
  if (!existsSync(cloudBackupPath)) {
    console.error("No .env.local.cloud backup found. Restore your cloud credentials manually.");
    process.exit(1);
  }

  copyFileSync(cloudBackupPath, envPath);
  console.log("Restored .env.local from .env.local.cloud");
}

if (mode === "local") {
  writeLocalEnv();
} else {
  writeCloudEnv();
}
